export const WeeklyDigestTemplate = ({
  userName = 'User',
  weekStart = '2024-01-01',
  weekEnd = '2024-01-07',
  crisisCount = 0,
  crises = [],
  dashboardUrl = 'https://bluerelief.com/dashboard',
}: WeeklyDigestProps) => {
  const formatDate = (dateString: string) => {
    return new Date(dateString).toLocaleDateString('en-US', {
      month: 'short',
      day: 'numeric',
      year: 'numeric'
    });
  };

  const getSeverityConfig = (severity?: string) => {
    const configs = {
      'critical': { color: '#ef4444', bg: '#fee2e2' },
      'high': { color: '#f97316', bg: '#ffedd5' },
      'medium': { color: '#f59e0b', bg: '#fef3c7' },
      'low': { color: '#22c55e', bg: '#dcfce7' },
    };
    return configs[severity?.toLowerCase() as keyof typeof configs] || { color: '#64748b', bg: '#f1f5f9' };
  };

  return (
    <Html>
      <Head />
      <Preview>Weekly Crisis Digest for {userName} - {String(crisisCount)} incidents reported</Preview>
      <Body style={main}>
        <Container style={container}>
          <Section style={logoContainer}>
            <Logo />
          </Section>
          
          <Section style={headerSection}>
            <Heading style={titleText}>Weekly Crisis Digest</Heading>
            <Text style={subtitleText}>
              {formatDate(weekStart)} - {formatDate(weekEnd)}
            </Text>
            <Text style={greetingText}>Hello {userName},</Text>
          </Section>
          
          <Section style={summarySection}>
            <Section style={summaryCard}>
              <Text style={summaryIcon}>üìä</Text>
              <Text style={summaryTitle}>This Week's Summary</Text>
              <Text style={crisisCountText}>
                <span style={countNumber}>{crisisCount}</span> crisis incidents reported
              </Text>
              {crisisCount > 0 && (
                <Text style={summarySubtext}>
                  Stay informed about the latest developments in your area.
                </Text>
              )}
            </Section>
          </Section>
          
          {crises.length > 0 && (
            <Section style={crisesSection}>
              <Text style={crisesTitle}>üö® Recent Incidents</Text>
              {crises.slice(0, 5).map((crisis, index) => (
                <Section key={index} style={crisisItem}>
                  <Section style={crisisHeader}>
                    <Text style={crisisType}>{crisis.type}</Text>
                    {crisis.severity && (
                      <Text style={{
                        ...severityTag,
                        backgroundColor: getSeverityConfig(crisis.severity).bg,
                        color: getSeverityConfig(crisis.severity).color,
                      }}>
                        {crisis.severity.toUpperCase()}
                      </Text>
                    )}
                  </Section>
                  <Text style={crisisLocation}>üìç {crisis.location}</Text>
                  <Text style={crisisDate}>üìÖ {formatDate(crisis.date)}</Text>
                </Section>
              ))}
              {crises.length > 5 && (
                <Section style={moreBox}>
                  <Text style={moreText}>
                    + {crises.length - 5} more incidents this week
                  </Text>
                </Section>
              )}
            </Section>
          )}
          
          <Section style={actionSection}>
            <Link style={dashboardButton} href={dashboardUrl}>
              View Full Dashboard
            </Link>
          </Section>
          
          <Hr style={divider} />
          
          <Section style={footerSection}>
            <Text style={footerText}>
              This weekly digest is automatically generated by BlueRelief.
            </Text>
            <Text style={footerText}>
              <Link href="https://bluerelief.com/settings" style={linkText}>
                Manage your notification preferences
              </Link>
            </Text>
          </Section>
        </Container>
      </Body>
    </Html>
  );
};

