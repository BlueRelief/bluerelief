interface Crisis {
  type: string;
  location: string;
  date: string;
  severity?: string;
}

interface WeeklyDigestProps {
  userName: string;
  weekStart: string;
  weekEnd: string;
  crisisCount: number;
  crises: Crisis[];
  dashboardUrl?: string;
}

